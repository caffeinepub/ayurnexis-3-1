export function findHerb(name: string): HerbMonograph | undefined {
  const lower = name.toLowerCase();
  return pharmacopeiaData.find(
    (h) =>
      h.name.toLowerCase().includes(lower) ||
      h.latinName.toLowerCase().includes(lower) ||
      lower.includes(h.name.toLowerCase().split(" ")[0].toLowerCase()),
  );
}

export function getCustomRef(
  herbId: string,
): Partial<HerbMonograph["parameters"]> | null {
  try {
    const stored = localStorage.getItem(`ayurnexis_custom_ref_${herbId}`);
    return stored ? JSON.parse(stored) : null;
  } catch {
    return null;
  }
}

export function saveCustomRef(
  herbId: string,
  params: Partial<HerbMonograph["parameters"]>,
): void {
  localStorage.setItem(
    `ayurnexis_custom_ref_${herbId}`,
    JSON.stringify(params),
  );
}
