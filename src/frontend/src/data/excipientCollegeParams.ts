// College-level pharmacopeial parameters for all APIs and excipients
// References: IP 2022, BP 2023, USP, WHO

export interface ExcipientCollegeParams {
  categoricalInfo: {
    chemicalClass: string;
    source: string;
    form: string;
  };
  organoleptic: {
    color: { value: string; unit: string; reference: string };
    odor: { value: string; unit: string; reference: string };
    taste: { value: string; unit: string; reference: string };
    texture: { value: string; unit: string; reference: string };
  };
  physicochemical: {
    moistureContent: { range: string; unit: string; reference: string };
    totalAsh?: { range: string; unit: string; reference: string };
    pH: { range: string; unit: string; reference: string };
    solubility: { value: string; unit: string; reference: string };
  };
  basicEvaluation: {
    foreignMatter?: { range: string; unit: string; reference: string };
    assayPurity: { range: string; unit: string; reference: string };
  };
}

const p = (
  chemicalClass: string,
  source: string,
  form: string,
  colorV: string,
  odorV: string,
  tasteV: string,
  textureV: string,
  moistureRange: string,
  pHRange: string,
  pHUnit: string,
  solubilityV: string,
  assayRange: string,
  assayUnit: string,
  totalAshRange?: string,
): ExcipientCollegeParams => ({
  categoricalInfo: { chemicalClass, source, form },
  organoleptic: {
    color: {
      value: colorV,
      unit: "Visual",
      reference: source.split("/")[0].trim(),
    },
    odor: {
      value: odorV,
      unit: "Organoleptic",
      reference: source.split("/")[0].trim(),
    },
    taste: {
      value: tasteV,
      unit: "Organoleptic",
      reference: source.split("/")[0].trim(),
    },
    texture: {
      value: textureV,
      unit: "Visual",
      reference: source.split("/")[0].trim(),
    },
  },
  physicochemical: {
    moistureContent: {
      range: moistureRange,
      unit: "%",
      reference: source.split("/")[0].trim(),
    },
    ...(totalAshRange
      ? {
          totalAsh: {
            range: totalAshRange,
            unit: "%",
            reference: source.split("/")[0].trim(),
          },
        }
      : {}),
    pH: {
      range: pHRange,
      unit: pHUnit,
      reference: source.split("/")[0].trim(),
    },
    solubility: {
      value: solubilityV,
      unit: "Qualitative",
      reference: source.split("/")[0].trim(),
    },
  },
  basicEvaluation: {
    assayPurity: {
      range: assayRange,
      unit: assayUnit,
      reference: source.split("/")[0].trim(),
    },
  },
});

